-- DIM_DATE - Calendar Dimension (Dynamic Table)
-- Derived from SILVER.SALES_HEADER date range
-- =====================================================
CREATE OR REPLACE DYNAMIC TABLE SALES_DEV.GOLD.DIM_DATE
    TARGET_LAG = DOWNSTREAM
    WAREHOUSE = COMPUTE_WH
    REFRESH_MODE = FULL
    INITIALIZE = ON_CREATE
    COMMENT = 'Date dimension derived from sales transaction date range for time-based analytics'
AS
SELECT DISTINCT
    SHA2(TO_VARCHAR(TRANSACTION_TIMESTAMP, 'YYYYMMDD'), 256) AS DATE_DIM_KEY,
    TRANSACTION_TIMESTAMP AS DATE_KEY,
    YEAR(TRANSACTION_TIMESTAMP) AS YEAR_NUMBER,
    QUARTER(TRANSACTION_TIMESTAMP) AS QUARTER_NUMBER,
    MONTH(TRANSACTION_TIMESTAMP) AS MONTH_NUMBER,
    WEEKOFYEAR(TRANSACTION_TIMESTAMP) AS WEEK_NUMBER,
    DAYOFYEAR(TRANSACTION_TIMESTAMP) AS DAY_OF_YEAR,
    DAYOFMONTH(TRANSACTION_TIMESTAMP) AS DAY_OF_MONTH,
    DAYOFWEEK(TRANSACTION_TIMESTAMP) AS DAY_OF_WEEK,
    TO_VARCHAR(TRANSACTION_TIMESTAMP, 'YYYY') AS YEAR_NAME,
    'Q' || QUARTER(TRANSACTION_TIMESTAMP) AS QUARTER_NAME,
    TO_VARCHAR(TRANSACTION_TIMESTAMP, 'MMMM') AS MONTH_NAME,
    TO_VARCHAR(TRANSACTION_TIMESTAMP, 'MON') AS MONTH_SHORT_NAME,
    TO_VARCHAR(TRANSACTION_TIMESTAMP, 'DY') AS DAY_NAME,
    TO_VARCHAR(TRANSACTION_TIMESTAMP, 'YYYY-MM') AS YEAR_MONTH,
    TO_VARCHAR(TRANSACTION_TIMESTAMP, 'YYYY') || '-Q' || QUARTER(TRANSACTION_TIMESTAMP) AS YEAR_QUARTER,
    CASE WHEN MONTH(TRANSACTION_TIMESTAMP) >= 4 THEN YEAR(TRANSACTION_TIMESTAMP) ELSE YEAR(TRANSACTION_TIMESTAMP) - 1 END AS FISCAL_YEAR,
    CASE WHEN MONTH(TRANSACTION_TIMESTAMP) >= 4 THEN QUARTER(TRANSACTION_TIMESTAMP) - 1 ELSE QUARTER(TRANSACTION_TIMESTAMP) + 3 END AS FISCAL_QUARTER,
    CASE WHEN DAYOFWEEK(TRANSACTION_TIMESTAMP) IN (0, 6) THEN TRUE ELSE FALSE END AS IS_WEEKEND,
    FALSE AS IS_HOLIDAY
FROM SALES_DEV.SILVER.SALES_HEADER
WHERE IS_VALID_RECORD = TRUE;

