-- =====================================================
-- FACT_SALES_HEADER - Transaction Level Fact
-- Grain: One row per transaction
-- =====================================================
CREATE OR REPLACE DYNAMIC TABLE SALES_DEV.GOLD.FACT_SALES_HEADER
    TARGET_LAG = DOWNSTREAM
    WAREHOUSE = COMPUTE_WH
    REFRESH_MODE = FULL
    INITIALIZE = ON_CREATE
    COMMENT = 'Sales header fact at transaction grain for transaction-level analytics'
AS
SELECT
    -- Fact Key
    SHA2(CONCAT(
        COALESCE(sh.TRANSACTION_ID, ''),
        COALESCE(TO_VARCHAR(sh.__BRONZE_LOAD_TS, 'YYYY-MM-DD HH24:MI:SS.FF6'), '')
    ), 256) AS FACT_SALES_HEADER_KEY,
    
    -- Dimension Keys from Gold Layer
    c.CUSTOMER_DIM_KEY,
    s.STORE_DIM_KEY,
    cn.COUNTRY_DIM_KEY,
    d.DATE_DIM_KEY,
    
    -- Degenerate Dimensions
    sh.TRANSACTION_ID,
    sh.TRANSACTION_NUMBER,
    
    -- Transaction Attributes
    sh.TRANSACTION_TIMESTAMP,
    sh.CHANNEL_ID,
    sh.PAYMENT_METHOD,
    sh.CURRENCY,
    
    -- Measures (Additive Facts)
    sh.GROSS_AMOUNT,
    sh.TOTAL_DISCOUNT,
    sh.TOTAL_TAX,
    sh.NET_TOTAL,
    
    -- Count Measure
    1 AS TRANSACTION_COUNT,
    
    -- Audit
    sh.__BRONZE_LOAD_TS AS __SOURCE_LOAD_TS

FROM SALES_DEV.SILVER.SALES_HEADER sh
JOIN SALES_DEV.GOLD.DIM_CUSTOMER c 
    ON sh.CUSTOMER_ID = c.CUSTOMER_ID AND c.IS_CURRENT = TRUE
JOIN SALES_DEV.GOLD.DIM_STORE s 
    ON sh.STORE_ID = s.STORE_CODE AND s.IS_CURRENT = TRUE
JOIN SALES_DEV.GOLD.DIM_COUNTRY cn 
    ON s.COUNTRY_CODE = cn.COUNTRY_CODE AND cn.IS_CURRENT = TRUE
JOIN SALES_DEV.GOLD.DIM_DATE d 
    ON sh.TRANSACTION_TIMESTAMP = d.DATE_KEY
WHERE sh.IS_VALID_RECORD = TRUE;

