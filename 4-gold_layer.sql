-- =====================================================
-- BRIDGE_PRODUCT_COUNTRY - Many-to-Many Bridge Table
-- Links products to countries where they are available
-- Prevents row explosion in DIM_PRODUCT
-- =====================================================
CREATE OR REPLACE DYNAMIC TABLE SALES_DEV.GOLD.BRIDGE_PRODUCT_COUNTRY
    TARGET_LAG = DOWNSTREAM
    WAREHOUSE = COMPUTE_WH
    REFRESH_MODE = INCREMENTAL
    INITIALIZE = ON_CREATE
    COMMENT = 'Bridge table linking products to countries where they are available - many-to-many relationship'
AS
SELECT
    -- Composite Key
    SHA2(CONCAT(
        COALESCE(pa.SKU_CODE, ''),
        COALESCE(pa.COUNTRY_CODE, ''),
        COALESCE(TO_VARCHAR(pa.__BRONZE_LOAD_TS, 'YYYY-MM-DD HH24:MI:SS.FF6'), '')
    ), 256) AS BRIDGE_KEY,
    
    -- Foreign Keys (for joining to dimensions)
    SHA2(CONCAT(
        COALESCE(pa.SKU_CODE, ''),
        COALESCE(TO_VARCHAR(ps.__BRONZE_LOAD_TS, 'YYYY-MM-DD HH24:MI:SS.FF6'), '')
    ), 256) AS PRODUCT_DIM_KEY,
    
    SHA2(CONCAT(
        COALESCE(pa.COUNTRY_CODE, ''),
        COALESCE(TO_VARCHAR(cm.__BRONZE_LOAD_TS, 'YYYY-MM-DD HH24:MI:SS.FF6'), '')
    ), 256) AS COUNTRY_DIM_KEY,
    
    -- Business Keys
    pa.SKU_CODE,
    pa.COUNTRY_CODE,
    
    -- Availability Attributes
    pa.LOCAL_LAUNCH_DATE,
    pa.LOCAL_DISCONTINUE_DATE,
    pa.IS_AVAILABLE,
    
    -- Audit
    pa.__BRONZE_LOAD_TS AS __SOURCE_LOAD_TS

FROM SALES_DEV.SILVER.PRODUCT_COUNTRY_AVAILABILITY pa
JOIN SALES_DEV.SILVER.PRODUCT_SKU_MASTER ps 
    ON pa.SKU_CODE = ps.SKU_CODE AND ps.IS_VALID_RECORD = TRUE
JOIN SALES_DEV.SILVER.COUNTRY_MASTER cm 
    ON pa.COUNTRY_CODE = cm.COUNTRY_CODE AND cm.IS_VALID_RECORD = TRUE
WHERE pa.IS_VALID_RECORD = TRUE;

